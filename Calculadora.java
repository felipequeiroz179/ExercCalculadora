package Calculadoraa;

public class Calculadoraa {
   double numeroA;
   double numeroB;
   double adicao;
   double subtracao;
   double multiplicacao;
   double divisao;
   
   void adicao(){
	   adicao = numeroA + numeroB;
   }
   
   void subtracao(){
	   subtracao = numeroA - numeroB;
   }
   
   void multiplicacao(){
	   multiplicacao = numeroA * numeroB;
   }
   
   void divisao(){
	   divisao = numeroA / numeroB;
   }
}
